"""Docket Monitor Agent — Real-time court docket tracking and auto-response.

Monitors court dockets for new entries, recalculates deadlines when
docket changes occur, and auto-triggers workflows (e.g., when opposing
counsel files a motion, the system starts drafting a response).

Data sources:
  - PACER RSS feeds (federal courts — free, near real-time)
  - CourtListener webhooks (free, 15-min delay)
  - PACER API polling (federal courts — paid, real-time)
  - State court portal polling (varies by state)

All configuration via environment variables. No hardcoded secrets.
"""

from __future__ import annotations

import asyncio
import logging
import os
import uuid
from datetime import datetime, timedelta
from enum import Enum
from typing import Any, Callable, Dict, List, Optional

from pydantic import BaseModel, Field

logger = logging.getLogger("cyphergy.agents.docket_monitor")


class DocketEntryType(str, Enum):
    MOTION = "motion"
    ORDER = "order"
    NOTICE = "notice"
    COMPLAINT = "complaint"
    ANSWER = "answer"
    DISCOVERY = "discovery"
    JUDGMENT = "judgment"
    SCHEDULING = "scheduling"
    HEARING = "hearing"
    APPEAL = "appeal"
    OTHER = "other"


class DocketEntry(BaseModel):
    """A single docket entry from a court case."""
    entry_id: str = Field(default_factory=lambda: f"dkt-{uuid.uuid4().hex[:8]}")
    case_id: str
    case_number: str
    court: str
    jurisdiction: str
    entry_number: int
    entry_date: str
    entry_type: str
    description: str
    filed_by: str = ""
    document_url: Optional[str] = None
    requires_response: bool = False
    response_deadline: Optional[str] = None
    is_new: bool = True
    processed: bool = False
    triggered_workflow: Optional[str] = None


class MonitoredCase(BaseModel):
    """A case being monitored by the Docket Monitor."""
    monitor_id: str = Field(default_factory=lambda: f"mon-{uuid.uuid4().hex[:8]}")
    case_id: str
    case_number: str
    court: str
    jurisdiction: str
    parties: Dict[str, str] = Field(default_factory=dict)
    monitoring_source: str = Field(default="courtlistener")
    last_checked: Optional[str] = None
    last_entry_number: int = 0
    active: bool = True
    alert_email: Optional[str] = None
    auto_respond: bool = True
    created_at: str = Field(default_factory=lambda: datetime.utcnow().isoformat())


class DocketAlert(BaseModel):
    """An alert generated by the Docket Monitor."""
    alert_id: str = Field(default_factory=lambda: f"alert-{uuid.uuid4().hex[:8]}")
    case_id: str
    entry: DocketEntry
    urgency: str = Field(description="critical, high, medium, low")
    plain_language_summary: str
    recommended_action: str
    deadline: Optional[str] = None
    auto_workflow_triggered: bool = False
    workflow_id: Optional[str] = None



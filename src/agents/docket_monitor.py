class DocketMonitor:
    """Monitors court dockets and triggers automated workflows.

    Usage::

        monitor = DocketMonitor()

        # Start monitoring a case
        await monitor.add_case(
            case_id="case-12345",
            case_number="1:26-cv-00123",
            court="SDFL",
            jurisdiction="USDC",
        )

        # Check for new entries
        alerts = await monitor.check_all()

        # Process alerts
        for alert in alerts:
            if alert.auto_workflow_triggered:
                print(f"Auto-drafting {alert.recommended_action}")
    """

    def __init__(self, agentcore_runtime: Any = None) -> None:
        self._runtime = agentcore_runtime
        self._monitored_cases: Dict[str, MonitoredCase] = {}
        self._workflow_callbacks: Dict[str, Callable] = {}
        logger.info("DocketMonitor initialized")

    async def add_case(
        self,
        case_id: str,
        case_number: str,
        court: str,
        jurisdiction: str,
        parties: Optional[Dict[str, str]] = None,
        auto_respond: bool = True,
    ) -> MonitoredCase:
        """Add a case to the monitoring queue."""
        monitor = MonitoredCase(
            case_id=case_id,
            case_number=case_number,
            court=court,
            jurisdiction=jurisdiction,
            parties=parties or {},
            auto_respond=auto_respond,
        )
        self._monitored_cases[monitor.monitor_id] = monitor
        logger.info("Now monitoring case %s (%s)", case_number, court)
        return monitor

    async def remove_case(self, monitor_id: str) -> None:
        """Stop monitoring a case."""
        if monitor_id in self._monitored_cases:
            self._monitored_cases[monitor_id].active = False
            logger.info("Stopped monitoring: %s", monitor_id)

    async def check_all(self) -> List[DocketAlert]:
        """Check all monitored cases for new docket entries."""
        alerts = []
        active_cases = [m for m in self._monitored_cases.values() if m.active]

        for case in active_cases:
            try:
                new_entries = await self._fetch_new_entries(case)
                for entry in new_entries:
                    alert = await self._process_entry(case, entry)
                    if alert:
                        alerts.append(alert)

                case.last_checked = datetime.utcnow().isoformat()
            except Exception as exc:
                logger.error("Error checking case %s: %s", case.case_number, str(exc)[:200])

        return alerts

    async def _fetch_new_entries(self, case: MonitoredCase) -> List[DocketEntry]:
        """Fetch new docket entries from the appropriate source."""
        if case.monitoring_source == "courtlistener":
            return await self._fetch_courtlistener(case)
        elif case.monitoring_source == "pacer_rss":
            return await self._fetch_pacer_rss(case)
        elif case.monitoring_source == "pacer_api":
            return await self._fetch_pacer_api(case)
        else:
            return await self._fetch_courtlistener(case)

    async def _fetch_courtlistener(self, case: MonitoredCase) -> List[DocketEntry]:
        """Fetch new entries from CourtListener API."""
        import aiohttp

        cl_token = os.getenv("COURTLISTENER_API_TOKEN", "")
        base_url = "https://www.courtlistener.com/api/rest/v4"

        entries = []
        try:
            async with aiohttp.ClientSession() as session:
                headers = {}
                if cl_token:
                    headers["Authorization"] = f"Token {cl_token}"

                params = {
                    "docket__case_name": case.case_number,
                    "order_by": "-date_filed",
                    "page_size": 20,
                }

                async with session.get(
                    f"{base_url}/docket-entries/",
                    params=params,
                    headers=headers,
                ) as resp:
                    if resp.status == 200:
                        data = await resp.json()
                        for result in data.get("results", []):
                            entry_num = result.get("entry_number", 0)
                            if entry_num > case.last_entry_number:
                                entries.append(DocketEntry(
                                    case_id=case.case_id,
                                    case_number=case.case_number,
                                    court=case.court,
                                    jurisdiction=case.jurisdiction,
                                    entry_number=entry_num,
                                    entry_date=result.get("date_filed", ""),
                                    entry_type=self._classify_entry(result.get("description", "")),
                                    description=result.get("description", ""),
                                ))

                        if entries:
                            case.last_entry_number = max(e.entry_number for e in entries)

        except Exception as exc:
            logger.error("CourtListener fetch error: %s", str(exc)[:200])

        return entries

    async def _fetch_pacer_rss(self, case: MonitoredCase) -> List[DocketEntry]:
        """Fetch new entries from PACER RSS feeds (free)."""
        import aiohttp
        import xml.etree.ElementTree as ET

        # PACER RSS feed URLs by court
        rss_urls = {
            "SDFL": "https://ecf.flsd.uscourts.gov/cgi-bin/rss_outside.pl",
            "NDGA": "https://ecf.gand.uscourts.gov/cgi-bin/rss_outside.pl",
            "SDNY": "https://ecf.nysd.uscourts.gov/cgi-bin/rss_outside.pl",
        }

        rss_url = rss_urls.get(case.court)
        if not rss_url:
            return []

        entries = []
        try:
            async with aiohttp.ClientSession() as session:
                async with session.get(rss_url) as resp:
                    if resp.status == 200:
                        xml_text = await resp.text()
                        root = ET.fromstring(xml_text)
                        for item in root.findall(".//item"):
                            title = item.findtext("title", "")
                            if case.case_number in title:
                                entries.append(DocketEntry(
                                    case_id=case.case_id,
                                    case_number=case.case_number,
                                    court=case.court,
                                    jurisdiction=case.jurisdiction,
                                    entry_number=case.last_entry_number + 1,
                                    entry_date=item.findtext("pubDate", ""),
                                    entry_type=self._classify_entry(title),
                                    description=title,
                                    document_url=item.findtext("link", ""),
                                ))
        except Exception as exc:
            logger.error("PACER RSS fetch error: %s", str(exc)[:200])

        return entries

    async def _fetch_pacer_api(self, case: MonitoredCase) -> List[DocketEntry]:
        """Fetch entries via PACER API (paid, real-time)."""
        pacer_url = os.getenv("PACER_API_URL", "")
        pacer_token = os.getenv("PACER_API_TOKEN", "")

        if not pacer_url or not pacer_token:
            return await self._fetch_courtlistener(case)

        # PACER API integration would go here
        return []

    async def _process_entry(
        self,
        case: MonitoredCase,
        entry: DocketEntry,
    ) -> Optional[DocketAlert]:
        """Process a new docket entry and generate an alert if needed."""
        # Classify the entry and determine if response is needed
        entry_type = entry.entry_type.lower()
        trigger = None

        for trigger_key, trigger_config in RESPONSE_TRIGGERS.items():
            if trigger_key in entry_type or trigger_key.replace("_", " ") in entry.description.lower():
                trigger = trigger_config
                break

        if trigger:
            entry.requires_response = trigger["requires_response"]
            # Calculate response deadline
            days = trigger.get(
                "federal_days" if case.jurisdiction == "USDC" else "default_state_days",
                21,
            )
            if entry.entry_date:
                try:
                    entry_dt = datetime.fromisoformat(entry.entry_date.replace("Z", "+00:00"))
                    deadline_dt = entry_dt + timedelta(days=days)
                    entry.response_deadline = deadline_dt.isoformat()
                except (ValueError, TypeError):
                    pass

        # Determine urgency
        urgency = self._assess_urgency(entry)

        # Generate plain-language summary
        summary = await self._generate_alert_summary(entry, case)

        # Determine recommended action
        action = self._recommend_action(entry, trigger)

        alert = DocketAlert(
            case_id=case.case_id,
            entry=entry,
            urgency=urgency,
            plain_language_summary=summary,
            recommended_action=action,
            deadline=entry.response_deadline,
        )

        # Auto-trigger workflow if enabled
        if case.auto_respond and entry.requires_response and trigger:
            alert.auto_workflow_triggered = True
            alert.workflow_id = f"wf-{uuid.uuid4().hex[:8]}"
            logger.info(
                "Auto-triggered workflow %s for entry %s",
                alert.workflow_id, entry.entry_id,
            )

        return alert

    def _classify_entry(self, description: str) -> str:
        """Classify a docket entry by type."""
        desc_lower = description.lower()
        if "motion" in desc_lower:
            return DocketEntryType.MOTION.value
        elif "order" in desc_lower:
            return DocketEntryType.ORDER.value
        elif "notice" in desc_lower:
            return DocketEntryType.NOTICE.value
        elif "complaint" in desc_lower:
            return DocketEntryType.COMPLAINT.value
        elif "answer" in desc_lower:
            return DocketEntryType.ANSWER.value
        elif "interrogator" in desc_lower or "production" in desc_lower or "admission" in desc_lower:
            return DocketEntryType.DISCOVERY.value
        elif "judgment" in desc_lower or "verdict" in desc_lower:
            return DocketEntryType.JUDGMENT.value
        elif "scheduling" in desc_lower or "calendar" in desc_lower:
            return DocketEntryType.SCHEDULING.value
        elif "hearing" in desc_lower:
            return DocketEntryType.HEARING.value
        elif "appeal" in desc_lower:
            return DocketEntryType.APPEAL.value
        return DocketEntryType.OTHER.value

    def _assess_urgency(self, entry: DocketEntry) -> str:
        """Assess the urgency of a docket entry."""
        desc_lower = entry.description.lower()
        if any(term in desc_lower for term in ["order to show cause", "emergency", "tro", "temporary restraining"]):
            return "critical"
        elif entry.requires_response:
            return "high"
        elif entry.entry_type in (DocketEntryType.ORDER.value, DocketEntryType.JUDGMENT.value):
            return "high"
        elif entry.entry_type == DocketEntryType.SCHEDULING.value:
            return "medium"
        return "low"

    def _recommend_action(
        self,
        entry: DocketEntry,
        trigger: Optional[Dict[str, Any]],
    ) -> str:
        """Recommend an action based on the docket entry."""
        if trigger and trigger.get("requires_response"):
            response_type = trigger.get("response_type", "response")
            return f"Draft {response_type.replace('_', ' ')} — deadline: {entry.response_deadline or 'TBD'}"
        elif entry.entry_type == DocketEntryType.ORDER.value:
            return "Review court order and determine if any action is required"
        elif entry.entry_type == DocketEntryType.SCHEDULING.value:
            return "Update calendar with new court dates"
        elif entry.entry_type == DocketEntryType.JUDGMENT.value:
            return "Review judgment and determine if appeal is warranted"
        return "Review new docket entry"

    async def _generate_alert_summary(
        self,
        entry: DocketEntry,
        case: MonitoredCase,
    ) -> str:
        """Generate a plain-language alert summary."""
        summary = f"New activity in your case ({case.case_number}): "

        if entry.requires_response:
            summary += (
                f"The other side filed a {entry.description}. "
                f"You need to respond by {entry.response_deadline or 'the deadline'}. "
                f"We're already working on a draft for you."
            )
        elif entry.entry_type == DocketEntryType.ORDER.value:
            summary += f"The judge issued an order: {entry.description}"
        elif entry.entry_type == DocketEntryType.SCHEDULING.value:
            summary += f"A court date has been scheduled: {entry.description}"
        else:
            summary += entry.description

        return summary

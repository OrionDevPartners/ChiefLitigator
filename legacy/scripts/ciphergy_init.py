def validate_asana(config):
    """Test Asana API connection if configured."""
    asana_config = config.get("asana", {})
    if not asana_config.get("enabled", False):
        print("  [SKIP] Asana integration disabled in config.")
        return False

    pat_var = asana_config.get("pat_env_var", "ASANA_PAT")
    pat = os.environ.get(pat_var)
    if not pat:
        print(f"  [WARN] Asana PAT not found in environment variable: {pat_var}")
        print(f"         Set it with: export {pat_var}=your_token")
        return False

    try:
        import urllib.request
        import urllib.error

        req = urllib.request.Request(
            "https://app.asana.com/api/1.0/users/me",
            headers={"Authorization": f"Bearer {pat}"},
        )
        with urllib.request.urlopen(req, timeout=10) as resp:
            data = json.loads(resp.read().decode())
            user_name = data.get("data", {}).get("name", "Unknown")
            print(f"  [OK] Asana connected as: {user_name}")
            return True
    except urllib.error.HTTPError as e:
        print(f"  [ERROR] Asana API returned HTTP {e.code}")
        return False
    except Exception as e:
        print(f"  [ERROR] Asana connection failed: {e}")
        return False


def post_asana_init(config, name):
    """Post initialization message to Asana if connected."""
    asana_config = config.get("asana", {})
    if not asana_config.get("enabled", False):
        return

    task_gid = asana_config.get("comm_task_gid")
    if not task_gid:
        print("  [SKIP] No Asana comm_task_gid configured.")
        return

    pat_var = asana_config.get("pat_env_var", "ASANA_PAT")
    pat = os.environ.get(pat_var)
    if not pat:
        return

    try:
        import urllib.request

        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        message = (
            f"[Ciphergy Pipeline] Project Initialized\n"
            f"Project: {name}\n"
            f"Time: {now}\n"
            f"Status: Ready for first sync"
        )

        payload = json.dumps({"data": {"text": message}}).encode()
        req = urllib.request.Request(
            f"https://app.asana.com/api/1.0/tasks/{task_gid}/stories",
            data=payload,
            headers={
                "Authorization": f"Bearer {pat}",
                "Content-Type": "application/json",
            },
            method="POST",
        )
        with urllib.request.urlopen(req, timeout=10) as resp:
            if resp.status == 201:
                print("  [OK] Asana initialization message posted.")
            else:
                print(f"  [WARN] Asana returned status {resp.status}")
    except Exception as e:
        print(f"  [WARN] Could not post to Asana: {e}")


def main():
    parser = argparse.ArgumentParser(description="Ciphergy Pipeline — Project Initializer")
    parser.add_argument("--name", required=True, help="Project name")
    parser.add_argument("--domain", default="custom", choices=list(DOMAINS.keys()), help="Project domain")
    parser.add_argument("--project-root", default=None, help="Project root directory")
    args = parser.parse_args()

    project_root = args.project_root or os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    config_path = os.path.join(project_root, "config", "ciphergy.yaml")
    registry_path = os.path.join(project_root, ".ciphergy", "registry.json")
    sync_dir = os.path.join(project_root, "sync")
    manifest_path = os.path.join(sync_dir, "SYNC_MANIFEST.md")

    print(f"\n  Initializing Ciphergy project: {args.name}")
    print(f"  Domain: {args.domain} ({DOMAINS[args.domain]['description']})")
    print(f"  Root: {project_root}")
    print()

    # Step 1: Config
    config = load_config(config_path)
    if config is None:
        config = create_default_config(config_path, args.name, args.domain)

    # Step 2: Template files
    create_templated_files(project_root, args.domain)

    # Step 3: Registry
    registry = create_registry(registry_path, project_root, args.name, args.domain, config)

    # Step 4: Sync baseline
    os.makedirs(sync_dir, exist_ok=True)
    create_sync_baseline(sync_dir, project_root, registry)

    # Step 5: Manifest
    create_sync_manifest(manifest_path, registry)

    # Step 6: Asana
    print()
    asana_ok = validate_asana(config)
    if asana_ok:
        post_asana_init(config, args.name)

    # Summary
    print()
    print("  " + "=" * 50)
    print(f"  Ciphergy Project Initialized Successfully")
    print("  " + "=" * 50)
    print(f"  Project:    {args.name}")
    print(f"  Domain:     {args.domain}")
    print(f"  Files:      {len(registry.get('files', []))} tracked")
    print(f"  Config:     {config_path}")
    print(f"  Registry:   {registry_path}")
    print(f"  Sync:       {sync_dir}/v1/")
    print(f"  Asana:      {'Connected' if asana_ok else 'Not configured'}")
    print()
    print("  Next steps:")
    print("    1. Review config/ciphergy.yaml and customize settings")
    print("    2. Upload sync/v1/ to your cloud agent")
    print("    3. Run: ciphergy.sh mark-synced")
    print("    4. Run: ciphergy.sh status")
    print()


if __name__ == "__main__":
    main()

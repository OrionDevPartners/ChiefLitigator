def create_registry(registry_path, project_root, name, domain, config):
    """Create .ciphergy/registry.json with file tracking."""
    domain_info = DOMAINS.get(domain, DOMAINS["custom"])

    # Discover existing files to track
    tracked_files = []
    for root, dirs, files in os.walk(project_root):
        # Skip hidden dirs and common excludes
        dirs[:] = [d for d in dirs if not d.startswith(".") and d not in ("node_modules", "__pycache__", "sync")]
        rel_root = os.path.relpath(root, project_root)
        for fname in files:
            if fname.startswith(".") or fname.endswith(".pyc"):
                continue
            rel_path = os.path.join(rel_root, fname) if rel_root != "." else fname
            full_path = os.path.join(root, fname)
            try:
                with open(full_path, "rb") as fh:
                    file_hash = hashlib.md5(fh.read()).hexdigest()
            except (IOError, OSError):
                file_hash = ""

            tracked_files.append({
                "path": rel_path,
                "hash": file_hash,
                "tracked_since": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
                "category": categorize_file(rel_path),
            })

    registry = {
        "project": {
            "name": name,
            "domain": domain,
            "initialized": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
        },
        "files": tracked_files,
        "agents": {
            agent_name: {
                "status": agent_info.get("status", "idle"),
                "type": agent_info.get("type", "unknown"),
                "last_active": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
            }
            for agent_name, agent_info in config.get("agents", {}).items()
        },
        "sync": {
            "current_version": "v1",
            "last_synced": "never",
            "synced_hashes": {},
        },
        "version_history": [
            {
                "version": "v1",
                "date": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ"),
                "trigger": "initialization",
                "files_changed": len(tracked_files),
            }
        ],
    }

    os.makedirs(os.path.dirname(registry_path), exist_ok=True)
    with open(registry_path, "w") as f:
        json.dump(registry, f, indent=2)

    print(f"  [OK] Created registry: {registry_path} ({len(tracked_files)} files tracked)")
    return registry


def categorize_file(path):
    """Categorize a file by its path/extension."""
    if path.startswith("scripts/"):
        return "script"
    elif path.startswith("docs/"):
        return "documentation"
    elif path.startswith("dashboard/"):
        return "dashboard"
    elif path.startswith("config/"):
        return "configuration"
    elif path.startswith("templates/"):
        return "template"
    elif path.endswith(".md"):
        return "document"
    elif path.endswith(".json"):
        return "data"
    elif path.endswith(".py"):
        return "script"
    elif path.endswith(".sh"):
        return "script"
    else:
        return "other"


def create_sync_baseline(sync_dir, project_root, registry):
    """Create sync/v1/ baseline with all tracked files."""
    v1_dir = os.path.join(sync_dir, "v1")
    os.makedirs(v1_dir, exist_ok=True)

    copied = 0
    for entry in registry.get("files", []):
        fpath = entry.get("path", "")
        src = os.path.join(project_root, fpath)
        if os.path.isfile(src):
            dest = os.path.join(v1_dir, os.path.basename(fpath))
            shutil.copy2(src, dest)
            copied += 1

    print(f"  [OK] Created sync baseline: {v1_dir} ({copied} files)")
    return v1_dir


def create_sync_manifest(manifest_path, registry):
    """Create SYNC_MANIFEST.md."""
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    file_count = len(registry.get("files", []))

    content = f"""# PROJECT KNOWLEDGE SYNC MANIFEST

## Base: v1 --- {now}

Total files: {file_count}
Current version: v1

## Version Log

| Version | Date | Trigger | Files Changed |
|---------|------|---------|---------------|
| v1 | {now} | Initial build | All files ({file_count}) |

## Instructions

1. Upload the contents of sync/v1/ to your cloud agent project.
2. After upload, run: `ciphergy.sh mark-synced`
3. For subsequent updates, run: `ciphergy.sh project-push`
4. Upload only the new version folder (e.g., sync/v2/).
5. After each upload, run: `ciphergy.sh mark-synced`

## Notes

- Each version folder contains only the files that changed since the last sync.
- The registry tracks hashes to detect changes automatically.
- Never modify files inside sync/v*/ folders directly.

---
*Generated by Ciphergy Pipeline on {now}*
"""

    with open(manifest_path, "w") as f:
        f.write(content)

    print(f"  [OK] Created manifest: {manifest_path}")


